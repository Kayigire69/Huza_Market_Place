const dictionaries: Record<Locale, Dict> = {
  en: { ...en, ...farmerAuthEn, ...farmerLandingEn, ...wsEn },
  fr: { ...fr, ...farmerAuthFr, ...farmerGapFr, ...farmerLandingFr, ...wsFr },
  rw: { ...rw, ...farmerAuthRw, ...farmerGapRw, ...farmerLandingRw, ...wsRw },
  sw: { ...sw, ...farmerAuthSw, ...farmerGapSw, ...farmerLandingSw, ...wsSw },
};

export function t(locale: Locale, key: string): string {
  return dictionaries[locale][key] ?? dictionaries.en[key] ?? key;
}

export function productName(
  product: { nameEn: string; nameFr: string; nameRw: string },
  locale: Locale
): string {
  if (locale === "fr") return product.nameFr;
  if (locale === "rw") return product.nameRw;
  return product.nameEn;
}

export function productDescription(
  product: { descriptionEn: string; descriptionFr: string; descriptionRw: string },
  locale: Locale
): string {
  if (locale === "fr") return product.descriptionFr;
  if (locale === "rw") return product.descriptionRw;
  return product.descriptionEn;
}

export function categoryName(
  category: { nameEn: string; nameFr: string; nameRw: string; slug?: string },
  locale: Locale
): string {
  if (locale === "fr") return category.nameFr;
  if (locale === "rw") {
    if (category.slug) return remapCategoryNameRw(category.slug, category.nameRw);
    return category.nameRw;
  }
  return category.nameEn;
}

